    private void adicionarTipoDoc() {
        String tipodoc = tipoDoctxt.getText();
        if (new TipoDocDAO().existe(tipodoc, processo.getId(), cliente.getId())) {
            if (!new TipoDocDAO().addTipoDoc(tipodoc, processo.getId(), cliente.getId())) {
                JOptionPane.showMessageDialog(null, "Erro ao tentar adicionar tipo de documento para cliente.", "Erro", JOptionPane.ERROR_MESSAGE);
            } else {
                dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Esse tipo de documento já existe para esse cliente.", "Processo Já Existente", JOptionPane.INFORMATION_MESSAGE);
        }
    }
