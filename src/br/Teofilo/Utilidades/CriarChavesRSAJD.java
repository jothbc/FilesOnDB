    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (caminhotxt.getText().equals("")) {
            return;
        }
        try {
            String caminho = caminhotxt.getText();
            final KeyPairGenerator keyGen = KeyPairGenerator.getInstance("RSA");
            keyGen.initialize(2048);
            final KeyPair key = keyGen.generateKeyPair();
            File chavePrivadaFile = new File(caminho + "private.key");
            File chavePublicaFile = new File(caminho + "public.key");
            chavePrivadaFile.createNewFile();
            chavePublicaFile.createNewFile();
            try ( //escreve no arquivo a chave publica
                    ObjectOutputStream chavePublicaOS = new ObjectOutputStream(
                            new FileOutputStream(chavePublicaFile)) // Salva a Chave Pública no arquivo
            ) {
                chavePublicaOS.writeObject(key.getPublic());
            }
            try ( //escreve no arquivo a chave privada
                    ObjectOutputStream chavePrivadaOS = new ObjectOutputStream(
                            new FileOutputStream(chavePrivadaFile)) // Salva a Chave Privada no arquivo
            ) {
                chavePrivadaOS.writeObject(key.getPrivate());
            }
            JOptionPane.showMessageDialog(null, "Chaves geradas com sucesso!");
            Desktop.getDesktop().open(new File(caminho));
        } catch (IOException | NoSuchAlgorithmException e) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar gerar as chaves.\n"+e,"Erro",JOptionPane.WARNING_MESSAGE);
            GerarLogErro.gerar(e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

