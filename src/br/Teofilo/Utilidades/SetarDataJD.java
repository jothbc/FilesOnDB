    private void definir() {
        try {
            Date data = (Date) jSpinner1.getValue();
            Calendar calendario = Calendar.getInstance();
            calendario.setTime(data);
            String[] dia = new String[3];
            String[] hora = new String[2];
            dia[0] = Integer.toString(calendario.get(Calendar.DAY_OF_MONTH));
            dia[1] = Integer.toString(calendario.get(Calendar.MONTH) + 1);
            dia[2] = Integer.toString(calendario.get(Calendar.YEAR));

            hora[0] = Integer.toString(calendario.get(Calendar.HOUR_OF_DAY));
            hora[1] = Integer.toString(calendario.get(Calendar.MINUTE));

            String d = "";
            if (dia[0].length() == 1) {
                d += "0" + dia[0] + "/";
            } else {
                d += dia[0] + "/";
            }
            if (dia[1].length() == 1) {
                d += "0" + dia[1] + "/";
            } else {
                d += dia[1] + "/";
            }
            d += dia[2];

            String h = "";
            if (hora[0].length() == 1) {
                h += "0" + hora[0] + ":";
            } else {
                h += hora[0] + ":";
            }
            if (hora[1].length() == 1) {
                h += "0" + hora[1] + ":00";
            } else {
                h += hora[1] + ":00";
            }
            User usuario = new UserDAO().getUser();
            usuario.setData(d);
            usuario.setHora(h);
            if (new UserDAO().setarControle_Comentarios(usuario)) {
                oculto.setText("Setado: " + d + "   " + h);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar gravar essas informa√ß√µes no banco de dados!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, "Verifique se as informa√ß√µes fornecidas est√£o corretas.");
            GerarLogErro.gerar(ex.getMessage());
        }
    }
