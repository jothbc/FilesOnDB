    private void preencherList() {
        lista.removeAllElements();
        destinatarios = new EmailDAO().getDestinatarios(ID_CARTAO);
        for (Email e : destinatarios) {
            lista.addElement(e);
        }
    }

    private void adicionarDestinatario() {
        if (new EmailDAO().addDestinatario(ID_CARTAO, emailtxt.getText())) {
            preencherList();
            emailtxt.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um problema ao tentar adicionar o destinat√°rio ao Banco de Dados.");
        }
    }

    private void removerDestinatario() {
        Email e = (Email) lista.getElementAt(jList1.getSelectedIndex());
        if (new EmailDAO().removerDestinatario(e.getId())) {
            preencherList();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um problema ao tentar remover o destinat√°rio do Banco de Dados.");
        }
    }
